
public class Nivell2App {

	public static void main(String[] args) {
		double pi=3.1416;
		int pi_int=(int)pi;
		float pi_float=(float)pi;
		String pi_str=String.valueOf(pi);
		System.out.println("Double: "+pi);
		System.out.println("Integer: "+pi_int);
		System.out.println("Float: "+pi_float);
		System.out.println("String: "+pi_str);
	}

}
